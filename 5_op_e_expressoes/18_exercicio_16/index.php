<?php

  /* ==  : igualdade, verifica se o valor é igaul
     !=  : diferença, verifica se é diferente
     === : idêntico, verifica se o valor e tipo são iguais
     !== : não idêntico, verifica se não é idêntico
  */

  $a = 5;
  $b = 3;

  if($a == $b) {
    echo "É igual! 1 <br>";
  }

  if($a != $b) {
    echo "É diferente! 2 <br>";
  }

  if($a === $b) {
    echo "É idêntico! 3 <br>";
  }

  if($a !== $b) {
    echo "Não é idêntico! 4";
  }

?>